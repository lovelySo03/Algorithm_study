# 예제 3-1 거스름돈
# 내 풀이 
n = 1260
m500 = n // 500 
m100 = (n - 500*(m500))//100
m50 = (n - 500*m500 - 100*m100)//50
m10 = (n - 500*m500 - 100*m100 - 50*m50) //10

print(f"500원 : {m500}")
print(f"100원 : {m100}")
print(f"50원 : {m50}")
print(f"10원 : {m10}")

